const LangContext = createContext(null);

export function LangProvider({ children }) {
    const [lang, setLang] = useState(() => localStorage.getItem('admin_lang') || 'uz');
    const changeLang = (l) => { localStorage.setItem('admin_lang', l); setLang(l); };
    const t = (key) => translations[lang]?.[key] ?? translations.uz[key] ?? key;
    return (
        <LangContext.Provider value={{ lang, changeLang, t }}>
            {children}
        </LangContext.Provider>
    );
}

export function useT() { return useContext(LangContext); }
